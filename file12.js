// booleans and comparison operator

let num1 = 4;
let num2 = 8;

console.log(num1 > num2); // prints false because num1 is not greater than num2
console.log(num1 <= num2); // prints true


// == vs ===

let number1 = 7;
let number2 = "7";
console.log(number1 == number2); // prints true because == only checks for values not the data types
console.log(number1 === number2); // pritns false because === checks for both values as well as the data types 

// != vs !==

console.log(number1 != number2); // prints false because != only checks for values not the data types
console.log(number1 !== number2); // pritns true because !== checks for both values as well as the data types 